class Charcase{
public static void main(String[] args){
char ch='A';
if (ch>='A' & ch<='Z'){
System.out.println("The character is an upper case");
}
else if (ch>='a' & ch<='z'){
System.out.print("The character is a lowercase");
}
else{
System.out.println("It is not a chcaracter");
}
}
}