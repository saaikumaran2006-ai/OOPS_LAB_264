class Data {
public static void main(String[] args) {

int age = 20;
System.out.println("Integer value: " + age);
        
short sht = 1234;
System.out.println("Short value: " + sht);      
   
long lng = 1234567898L;
System.out.println("Long value: " + lng);

double dbl = 99.99;
System.out.println("Double value: " + dbl);

float per = 85.5f;
System.out.println("Float value: " + per);

char grade = 'A';
System.out.println("Character value: " + grade);

boolean isJavaFun = true;
System.out.println("Boolean value: " + isJavaFun);

String name = "Saai Kumaran";
System.out.println("String value: " + name);
}
}
