class Dataoperation {
public static void main(String[] args) {

int a=10;
int b=3;

System.out.println(a+b);
System.out.println(a-b);
System.out.println(a*b);
System.out.println(a/b);
System.out.println(a%b);

long l1=100000L;
long l2=50000L;
System.out.println(l1+l2);
System.out.println(l1-l2);
System.out.println(l1*l2);
System.out.println(l1/l2);

float f1=10.5f;
float f2=2.5f;
System.out.println(f1+f2);
System.out.println(f1-f2);
System.out.println(f1*f2);
System.out.println(f1/f2);

double d1=20.75;
double d2=4.25;
System.out.println(d1+d2);
System.out.println(d1-d2);
System.out.println(d1*d2);
System.out.println(d1/d2);
}
}
